export function normalizeLocale(value: string | null | undefined): LocaleKey {
  return localeOptions.some((option) => option.key === value) ? (value as LocaleKey) : defaultLocale;
}

export function loadSavedLocale(storage: Storage | undefined = globalThis.localStorage): LocaleKey {
  return normalizeLocale(storage?.getItem(localeStorageKey));
}

export function saveLocale(locale: LocaleKey, storage: Storage | undefined = globalThis.localStorage) {
  storage?.setItem(localeStorageKey, locale);
}

export function translate(locale: LocaleKey, key: TranslationKey, params: TranslationParams = {}) {
  const template = dictionaries[locale][key] ?? dictionaries[defaultLocale][key];
  return template.replace(/\{([^}]+)\}/g, (match, name: string) => String(params[name] ?? match));
}
